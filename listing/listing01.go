package main

import (
	"fmt"
)

// инициализируется слайс a числами
// далее инициализируется слайс b за счет среза слайса а. "нарезание" не копирует данные среза, а создает новое значение среза, которое указывает на исходный массив
// поэтому при измении элементов b изменятся и элементы a
// вывод программы - [77 78 79]

func main() {
	a := [5]int{76, 77, 78, 79, 80}
	var b []int = a[1:4]
	fmt.Println(b)
}
